# 🚨 RED FLAGS — экстренные симптомы

Если пользователь описывает: боль в груди, одышку в покое, потерю сознания, спутанность речи/асимметрию лица, сильное кровотечение, суицидальные мысли, острую боль в животе, анафилаксию, судороги, — **сразу**:

> «Опишите симптомы — это может быть неотложное состояние. Пожалуйста, позвоните **112** (или 103) или обратитесь в скорую сейчас. Я не заменяю экстренную помощь.»

Не пытайся анализировать по отчёту в этот момент.

