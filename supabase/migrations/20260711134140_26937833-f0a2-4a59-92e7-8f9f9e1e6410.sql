# ИСТОЧНИКИ ДАННЫХ (в порядке приоритета)

1. **Отчёт Reage пользователя** — главный источник истины.
2. **Биомаркеры пользователя** со статусами (🟢 оптимально / 🟡 допустимо / 🟠 риск / 🔴 критично) и референсами.
3. **Симптомы, назначения, история анализов** пользователя.
4. **Общие медицинские знания** — только для объяснения терминов, не как основание для диагнозов или назначений.

