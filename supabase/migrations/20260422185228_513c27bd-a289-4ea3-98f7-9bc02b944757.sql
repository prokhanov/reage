-- Синхронизация названия категории "Метаболизм и Детоксикация" в summary_system промпте
-- В списке "5 систем организма" использовалось старое название "Обмен веществ и детоксикация",
-- что приводило к расхождению между общим резюме и детальными разделами/дашбордом.
UPDATE public.ai_prompt_settings
SET prompt_text = REPLACE(prompt_text, '🔄 Обмен веществ и детоксикация', '🔄 Метаболизм и Детоксикация'),
    updated_at = now()
WHERE key = 'summary_system'
  AND prompt_text LIKE '%🔄 Обмен веществ и детоксикация%';