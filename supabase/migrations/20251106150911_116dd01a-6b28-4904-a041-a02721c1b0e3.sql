-- Create medical conditions templates table for admin management
CREATE TABLE IF NOT EXISTS public.medical_conditions_templates (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  category TEXT NOT NULL,
  condition TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  UNIQUE(category, condition)
);

-- Enable RLS
ALTER TABLE public.medical_conditions_templates ENABLE ROW LEVEL SECURITY;

-- Policies for medical_conditions_templates
CREATE POLICY "Anyone can view medical condition templates"
  ON public.medical_conditions_templates
  FOR SELECT
  USING (true);

CREATE POLICY "Superadmins can manage medical condition templates"
  ON public.medical_conditions_templates
  FOR ALL
  USING (has_role(auth.uid(), 'superadmin'::app_role));

-- Add RLS policies for biomarkers management by superadmin
CREATE POLICY "Superadmins can insert biomarkers"
  ON public.biomarkers
  FOR INSERT
  WITH CHECK (has_role(auth.uid(), 'superadmin'::app_role));

CREATE POLICY "Superadmins can update biomarkers"
  ON public.biomarkers
  FOR UPDATE
  USING (has_role(auth.uid(), 'superadmin'::app_role));

CREATE POLICY "Superadmins can delete biomarkers"
  ON public.biomarkers
  FOR DELETE
  USING (has_role(auth.uid(), 'superadmin'::app_role));

-- Trigger for updated_at on medical_conditions_templates
CREATE TRIGGER update_medical_conditions_templates_updated_at
  BEFORE UPDATE ON public.medical_conditions_templates
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_updated_at();

