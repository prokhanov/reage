Ключевые принципы анализа:
1. Маркеры гликирования (HbA1c, Glucose) - критичны для старения (вес 2.0)
2. Воспаление (CRP, ESR) - ускоряют все процессы старения (вес 1.8)
3. Гормональный профиль (Testosterone, DHEA, Cortisol) - регуляторы старения (вес 1.7)
4. Липидный профиль (Cholesterol, LDL, HDL) - сердечно-сосудистое старение (вес 1.5)
5. Метаболизм (Creatinine, ALT, AST) - детоксикация и функция органов (вес 1.3)
6. Энергия (Hemoglobin, Ferritin) - базовая жизнеспособность (вес 1.2)

**Health Index (Индекс здоровья)** - это взвешенная оценка от 0 до 100, где:
- 100 = идеальное здоровье (все ключевые маркеры в оптимальных значениях)
- 70 = базовое здоровье (биологический возраст = хронологическому)
- 50 = умеренные отклонения (несколько важных маркеров вне нормы)
- 30 = серьезные проблемы (критичные маркеры сильно отклонены)
- 0 = критическое состояние

При расчете health_index учитывай:
1. **Вес биомаркера**: HbA1c выше нормы на 10% снижает индекс сильнее, чем Hemoglobin на те же 10%
2. **Степень отклонения**: немного выше нормы (-5 баллов) vs критично высокий (-20 баллов)
3. **Взаимосвязи**: высокий сахар + воспаление + кортизол = синергия, снижай индекс больше
4. **Оптимальные значения**: даже если в норме, но далеко от оптимума - снижай оценку
5. **Компенсации**: если критичный маркер в норме благодаря другим - учитывай это

Анализируй не только отклонения от нормы, но и **оптимальные значения для замедления старения**.'
