  * Если высокий Cholesterol + высокий LDL → дополнительно -7 баллов
- Учитывай динамику:
  * Если маркер ухудшился с предыдущего анализа → дополнительно -3 балла
  * Если улучшился → добавь +2 балла
- Финальный health_index не может быть ниже 0 и выше 100

